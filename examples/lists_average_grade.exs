# Խնդիր. միջին գնահատական
#
# Տրված է աշակերտի գնահատականների list-ը (10 բալանոց համակարգ)։
# Հաշվեք գնահատականների գումարը և միջինը։
#
# Գործարկելու համար՝
#   elixir lists_average_grade.exs
#
# defmodule-ի և def-ի մասին մանրամասն կխոսենք առաջիկա հոդվածներում.
# առայժմ բավական է իմանալ, որ դրանք մոդուլ և ֆունկցիա սահմանելու ձևեր են։
# Կենտրոնացեք run ֆունկցիայի մարմնի վրա։

defmodule AverageGrade do
  def run do
    grades = [8, 9, 10, 7, 9, 10]

    # Enum.sum/1-ը գումարում է list-ի բոլոր տարրերը։
    # Այն անցնում է ամբողջ list-ով, ուստի O(n) գործողություն է։
    sum = Enum.sum(grades)

    # length/1-ը ծանոթ է հոդվածից. այն նույնպես O(n) է՝
    # տարրերը հաշվելու համար պետք է անցնել ամբողջ շղթայով։
    count = length(grades)

    # Բաժանումը `/` օպերատորով միշտ վերադարձնում է float,
    # ինչպես տեսել ենք թվային տիպերի հոդվածում։
    average = sum / count

    IO.puts("Grades: #{inspect(grades, charlists: :as_list)}")
    IO.puts("Sum: #{sum}")
    IO.puts("Average: #{average}")
  end
end

AverageGrade.run()

